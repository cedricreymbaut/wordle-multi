// Filtre pour ne garder que les mots de exactement 5 lettres
export const VALID_WORDS = [...new Set(
  WORD_LIST.filter(w => w.length === 5 && /^[A-Z]+$/.test(w))
)];

export function getRandomWord(): string {
  return VALID_WORDS[Math.floor(Math.random() * VALID_WORDS.length)];
}

export function isValidWord(word: string): boolean {
  return VALID_WORDS.includes(word.toUpperCase());
}
